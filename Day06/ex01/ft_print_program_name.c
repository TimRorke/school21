void	ft_putchar(char c);

int		main(int argc, char **argv)
{
	int a;

	a = 0;
	while (argv[0][a] != '\0')
	{
		ft_putchar(argv[0][a]);
		a++;
	}
	ft_putchar('\n');
	return (0);
}
