#include <unistd.h>

int main(int ac, char **av)
{
	int i;
	int j;
	int n;

	i = 0;
	j = 0;
	if (ac != 3)
	{
		write(1, "\n", 1);
		return (0);
	}
	while (av[2][i] != '\0')
	{
		if (av[1][j] == av[2][i])
		{
			if (j > 0)
			{
				n = j;
				while (n < 0)
				{
					if (av[1][n] == av[2][i])
						i++;
					n--;
				}
			}
			write(1, &av[2][i], 1);
			j++;
		}
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
