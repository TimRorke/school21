int		ft_atoi(char *str);

int		ft_atoi(char *str)
{
	int rez;
	int a;
	int b;

	rez = 0;
	a = 0;
	b = 1;
	while (str[a] == 32 && str[a] != '\0')
	{
		a++;
	}
	if (str[a] == '-')
	{
		b = -1;
		a++;
	}
	while (str[a] >= '0' && str[a] <= '9')
	{
		rez = rez * 10 + str[a] - 48;
		a++;
	}
	return (rez * b);
}
